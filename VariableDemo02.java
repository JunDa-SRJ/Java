public class VariableDemo02{
     public static void main(String[] args){
//定义byte类型的变量
    byte b = 20;
	System.out.println(b);
//定义short类型的变量	
    short c = 100;
	System.out.println(c);
//定义int类型的变量
    int d = 10000;
    System.out.println(d);   	
//定义double类型的变量
    double e = 13.14;
	System.out.println(e);
//定义char类型的变量
    char f = 'a';
	System.out.println(f);
//定义boolean类型的变量
    boolean g = true;
    System.out.println(g);
//定义long类型的变量
//在数值后加L，让编译器知道为long类型，否则默认为int类型，整数过大则错误
    long h = 1000000000L;
     System.out.println(h);
//定义float类型
//在数值后加F，让编译器知道为float类型，否则默认为double类型,数值不兼容
   float l = 13.14F;
   System.out.println(l);
	 
	 
	 
	 
	 }
	 
}