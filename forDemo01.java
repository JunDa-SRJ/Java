/*
    输出数据
	
	需求：
	    在控制台输出1-5和5-1
*/
 
public class forDemo01 {
    public static void main(String[] args){
	//输出1-5
	for(int i = 1;i <= 5;i++){
	      System.out.println(i);
	}
	
	System.out.println("---------");
	//输出5-1
	for(int i  = 5;i >= 1;i--){
	      System.out.println(i);
	
	}
	
	
	
	}




} 